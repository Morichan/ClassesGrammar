package feature.value.expression.symbol;

/**
 * <p> 乗算クラス </p>
 *
 * <p>
 *     演算子における乗算{@code "*"}クラスです。
 * </p>
 */
public class Multi extends Symbol {

    /**
     * <p> 乗算演算子の文字列を取得します。 </p>
     *
     * @return "*"
     */
    @Override
    public String toString() {
        return "*";
    }
}
