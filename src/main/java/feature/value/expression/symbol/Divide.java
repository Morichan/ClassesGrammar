package feature.value.expression.symbol;

/**
 * <p> 除算クラス </p>
 *
 * <p>
 *     演算子における除算{@code "/"}クラスです。
 * </p>
 */
public class Divide extends Symbol {

    /**
     * <p> 除算演算子の文字列を取得します。 </p>
     *
     * @return "/"
     */
    @Override
    public String toString() {
        return "/";
    }
}
