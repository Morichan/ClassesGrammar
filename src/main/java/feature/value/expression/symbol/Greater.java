package feature.value.expression.symbol;

/**
 * <p> 大なりクラス </p>
 *
 * <p>
 *     演算子における大なり{@code ">"}クラスです。
 * </p>
 */
public class Greater extends Symbol {

    /**
     * <p> 大なり演算子の文字列を取得します。 </p>
     *
     * @return ">"
     */
    @Override
    public String toString() {
        return ">";
    }
}
