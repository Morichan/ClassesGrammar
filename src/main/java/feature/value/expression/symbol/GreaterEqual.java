package feature.value.expression.symbol;

/**
 * <p> 大なりイコールクラス </p>
 *
 * <p>
 *     演算子における大なりイコール{@code ">="}クラスです。
 * </p>
 */
public class GreaterEqual extends Symbol {

    /**
     * <p> 大なりイコール演算子の文字列を取得します。 </p>
     *
     * @return ">="
     */
    @Override
    public String toString() {
        return ">=";
    }
}
