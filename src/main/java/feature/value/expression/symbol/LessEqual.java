package feature.value.expression.symbol;

/**
 * <p> 小なりイコールクラス </p>
 *
 * <p>
 *     演算子における小なりイコール{@code "<="}クラスです。
 * </p>
 */
public class LessEqual extends Symbol {

    /**
     * <p> 小なりイコール演算子の文字列を取得します。 </p>
     *
     * @return "<="
     */
    @Override
    public String toString() {
        return "<=";
    }
}
