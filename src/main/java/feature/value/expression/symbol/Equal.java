package feature.value.expression.symbol;

/**
 * <p> 等号クラス </p>
 *
 * <p>
 *     演算子における等号{@code "=="}クラスです。
 * </p>
 */
public class Equal extends Symbol {

    /**
     * <p> 等号演算子の文字列を取得します。 </p>
     *
     * @return "=="
     */
    @Override
    public String toString() {
        return "==";
    }
}
