package feature.value.expression.symbol;

/**
 * <p> 不等号クラス </p>
 *
 * <p>
 *     演算子における不等号{@code "!="}クラスです。
 * </p>
 */
public class NotEqual extends Symbol {

    /**
     * <p> 不等号演算子の文字列を取得します。 </p>
     *
     * @return "!="
     */
    @Override
    public String toString() {
        return "!=";
    }
}
