package feature.value.expression.symbol;

/**
 * <p> 法クラス </p>
 *
 * <p>
 *     演算子における法{@code "%"}クラスです。
 * </p>
 */
public class Mod extends Symbol {

    /**
     * <p> 法演算子の文字列を取得します。 </p>
     *
     * @return "%"
     */
    @Override
    public String toString() {
        return "%";
    }
}
